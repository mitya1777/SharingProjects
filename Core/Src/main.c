uint16_t U1[0x746];
uint16_t U2[0x746];
uint8_t flag_UART_start = 0x00;



void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_ADC1_Init(void);
static void MX_DAC1_Init(void);
static void MX_TIM6_Init(void);

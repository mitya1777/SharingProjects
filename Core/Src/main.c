int main(void)
{
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_DAC1_Init();
  MX_TIM6_Init();
  MX_USART2_UART_Init();

  while (1)
  {
	  if (flag_UART_start == 0x01)
	  {
		  for (uint8_t i = 0x00; i < 0x747; i++)
		  {
			  USART2 -> TDR = U1[i];
		  }

	  flag_UART_start = 0x00;
	  }

  }
}

